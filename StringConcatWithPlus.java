import java.util.*;
public class StringConcatWithPlus
{
    public static void main(String[]args)
    {
        String s1="Hello ";
        String s2="This is Nitesh";
        String s3=null;
        String s4=null;
        s3=s1+s2;
        s4="This is "+"New String";
        System.out.println("s3= "+s3);
        System.out.println("s4= "+s4);
    }
}
